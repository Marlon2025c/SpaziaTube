    <h1>Présentation</h1>
    <h4>Depuis toujours, le règlement de la communauté YouTube décrit les types de contenus interdits sur la plate-forme. Il s'applique à tous les types de contenus, comme les vidéos, les commentaires, les liens et les miniatures, et fait partie intégrante de notre ensemble de règles. Nous l'évaluons régulièrement avec l'aide d'experts externes et de créateurs pour faire face aux nouveaux problèmes qui se posent sur la plate-forme.</h4>

    <h4>Pour faire appliquer le règlement de la communauté, nous nous appuyons sur des examinateurs manuels et sur des systèmes de machine learning (apprentissage automatique) qui traitent tous les utilisateurs de la même manière, quels que soient leur histoire, leur opinion, leur emplacement ou leur affiliation.</h4>

    <h4>Nos règles ont été élaborées pour rendre la communauté YouTube plus sûre, tout en donnant aux créateurs la liberté de partager leurs expériences et leurs points de vue divers et variés.</h4>
